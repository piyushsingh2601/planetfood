 public void showDataInTable() {
        try {
            ArrayList<Employees> empList = EmployeesDao.getAllEmpData();
            if (empList.isEmpty() == true) {
                JOptionPane.showMessageDialog(null, "Sorry!No Employee Present", "Error!", JOptionPane.INFORMATION_MESSAGE);
            } else {
                Object[] rows = new Object[5];
                DefaultTableModel model = (DefaultTableModel) jtEmpDetails.getModel();
                for (Employees e : empList) {
                    rows[0] = e.getEmpId();
                    rows[1] = e.getEmpName();
                    rows[2] = e.getJob();
                    rows[3] = e.getSal();
                    model.addRow(rows);
                }
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error While viewing all Employees!", "Exception!", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }

    }
